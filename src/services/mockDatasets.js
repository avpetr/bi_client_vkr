// Import custom datasets from CSV uploads
import { getCustomDatasets } from './csvParser';

// Import real-time dataset definitions
import { getRealtimeDatasets } from './realTimeAdapters';

// Helper function to get all datasets (built-in + custom + real-time)
export const getAllDatasets = () => {
    const builtIn = datasetRegistry;
    const custom = getCustomDatasets();
    const realTime = getRealtimeDatasets();

    // Convert custom datasets to registry format
    const customRegistry = {};
    custom.forEach(ds => {
        customRegistry[ds.id] = ds;
    });

    // Real-time datasets come last so they can be overridden
    return { ...builtIn, ...customRegistry, ...realTime };
};

// Helper function to get dataset by ID
export const getDatasetById = (datasetId) => {
    const allDatasets = getAllDatasets();
    return allDatasets[datasetId] || datasetRegistry.salesPerformance;
};

// Helper function to get all dataset options for selector
export const getDatasetOptions = () => {
    const allDatasets = getAllDatasets();
    const options = Object.values(allDatasets).map(ds => ({
        value: ds.id,
        label: ds.name,
        description: ds.description,
        isCustom: ds.isCustom || false,
        isRealTime: ds.isRealTime || false,
    }));

    // Sort: built-in first, then real-time, then custom
    return options.sort((a, b) => {
        if (a.isCustom !== b.isCustom) return a.isCustom ? 1 : -1;
        return 0;
    });
};

// Helper function to get metrics for a specific dataset
export const getMetricsForDataset = (datasetId) => {
    const dataset = getDatasetById(datasetId);
    return dataset.metrics;
};

// Get actual dataset data by ID (for filters and column extraction)
export const getDataById = (datasetId) => {
    const datasets = {
        'salesPerformance': salesPerformanceData,
        'userAnalytics': userAnalyticsData,
        'productCategories': productCategoriesData,
        'trafficSources': trafficSourcesData,
        'regionalSales': regionalSalesData,
        'financialMetrics': financialMetricsData,
        'customerSatisfaction': customerSatisfactionData,
        'employeePerformance': employeePerformanceData
    };

    // Check built-in datasets first
    if (datasets[datasetId]) {
        return datasets[datasetId];
    }

    // Check custom datasets
    const customDatasets = getCustomDatasets();
    const customDataset = customDatasets.find(ds => ds.id === datasetId);
    return customDataset ? customDataset.data : null;
};

